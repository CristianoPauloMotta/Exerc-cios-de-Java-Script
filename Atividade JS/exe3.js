function calcularAreaQuadrado(base, altura){
    let areatotal = base*altura
    return areatotal
}
console.log(calcularAreaQuadrado(15, 35))


function calcularAreaCubica(base, altura, largura){
    let cubagemtotal = base*altura*largura
    return cubagemtotal
}

console.log(calcularAreaCubica(4, 8, 12))